namespace WebMarkupMin.Core
{
	/// <summary>
	/// Render mode of HTML empty tag
	/// </summary>
	public enum HtmlEmptyTagRenderMode
	{
		/// <summary>
		/// Without slash
		/// </summary>
		NoSlash = 0,

		/// <summary>
		/// With slash
		/// </summary>
		Slash = 1,

		/// <summary>
		/// With space and slash
		/// </summary>
		SpaceAndSlash
	}
}